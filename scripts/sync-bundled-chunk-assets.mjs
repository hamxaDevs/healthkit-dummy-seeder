/**
 * Writes src/generated/bundledChunkAssets.ts with require() per chunk-*.hkchunk (Metro **asset** id, not inlined JSON).
 * Files are still UTF-8 JSON text; parse at runtime with parseImportJsonText after readAsStringAsync.
 */
import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'fs'
import { dirname, join } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const root = join(__dirname, '..')
const manifestPath = join(root, 'assets/apple_health_import.chunks/manifest.json')
const outFile = join(root, 'src/generated/bundledChunkAssets.ts')

function writeStub(reason) {
  mkdirSync(dirname(outFile), { recursive: true })
  const body = `/**
 * ${reason}
 * Run: npm run import:from-apple-export
 */
export const BUNDLED_CHUNK_TOTAL_SAMPLES = 0

export const BUNDLED_CHUNK_ASSET_MODULES: number[] = []
`
  writeFileSync(outFile, body, 'utf8')
  console.error(`[sync-bundled-chunk-assets] ${outFile} (${reason})`)
}

if (!existsSync(manifestPath)) {
  writeStub('No manifest — add export.xml under assets/apple_health_export/, then run import:from-apple-export.')
  process.exit(0)
}

let manifest
try {
  manifest = JSON.parse(readFileSync(manifestPath, 'utf8'))
} catch (e) {
  console.error('[sync-bundled-chunk-assets] Invalid manifest.json:', e)
  process.exit(1)
}

const chunks = manifest.chunks
if (!Array.isArray(chunks) || chunks.length === 0) {
  writeStub('manifest.json has no chunks.')
  process.exit(0)
}

if (chunks.some((c) => typeof c === 'string' && c.endsWith('.json'))) {
  writeStub('Manifest lists chunk-*.json — re-run npm run import:from-apple-export to emit .hkchunk (avoids Metro bundle string limit).')
  console.error('[sync-bundled-chunk-assets] Re-run: npm run import:from-apple-export')
  process.exit(0)
}

for (const name of chunks) {
  if (typeof name !== 'string' || !/^chunk-\d+\.hkchunk$/.test(name)) {
    console.error('[sync-bundled-chunk-assets] Bad chunk name in manifest:', name)
    process.exit(1)
  }
  const abs = join(root, 'assets/apple_health_import.chunks', name)
  if (!existsSync(abs)) {
    console.error('[sync-bundled-chunk-assets] Missing file:', name)
    process.exit(1)
  }
}

const totalSamples = typeof manifest.totalSamples === 'number' ? manifest.totalSamples : 0
const requires = chunks
  .map((name) => `  require('../../assets/apple_health_import.chunks/${name}') as number,`)
  .join('\n')

const out = `/**
 * Auto-generated by scripts/sync-bundled-chunk-assets.mjs — do not edit.
 */
export const BUNDLED_CHUNK_TOTAL_SAMPLES = ${totalSamples}

export const BUNDLED_CHUNK_ASSET_MODULES: number[] = [
${requires}
]
`

mkdirSync(dirname(outFile), { recursive: true })
writeFileSync(outFile, out, 'utf8')
console.error(`[sync-bundled-chunk-assets] ${chunks.length} .hkchunk asset(s) → bundledChunkAssets.ts (${totalSamples} samples).`)
